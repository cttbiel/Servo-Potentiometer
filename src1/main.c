void setServoAngle(uint16_t angle) {
	uint16_t pwmValue = (500 +(angle*2000)/270);
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1,pwmValue);
}

int main(void)
{
  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_USART3_UART_Init();
  MX_USB_OTG_FS_PCD_Init();
  MX_ADC3_Init();
  MX_TIM3_Init();
  MX_ADC1_Init();
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
  while (1)
  {
	  setServoAngle(0);
	  HAL_Delay(1000);
	  setServoAngle(135);
	  HAL_Delay(1000);
	  setServoAngle(270);
	  HAL_Delay(1000);
  }
}
